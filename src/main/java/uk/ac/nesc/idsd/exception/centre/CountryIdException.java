package uk.ac.nesc.idsd.exception.centre;

public class CountryIdException extends Exception {

    public CountryIdException() {

    }

    public CountryIdException(String message) {
        super(message);

    }

    public CountryIdException(Throwable cause) {
        super(cause);

    }

    public CountryIdException(String message, Throwable cause) {
        super(message, cause);

    }

}
