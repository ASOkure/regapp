package uk.ac.nesc.idsd.exception.centre;

public class CountryNameException extends Exception {

    public CountryNameException() {

    }

    public CountryNameException(String message) {
        super(message);

    }

    public CountryNameException(Throwable cause) {
        super(cause);

    }

    public CountryNameException(String message, Throwable cause) {
        super(message, cause);

    }

}
